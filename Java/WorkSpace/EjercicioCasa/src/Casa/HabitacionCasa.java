package Casa;

public class HabitacionCasa {

	public String tipo;
	public String tamaño;
	public String muebles;
	public String luminosidad;
	public String espaciosa;
	
	public HabitacionCasa(String tipo, String tamaño, String muebles, String luminosidad, String espaciosa) {
		super();
		this.tipo = tipo;
		this.tamaño = tamaño;
		this.muebles = muebles;
		this.luminosidad = luminosidad;
		this.espaciosa = espaciosa;
	}
	
	
	
	
}
